vector<Triangle*> MarchingCubes::March(float st)
{
    Surface = st;
    //#pragma omp parallel for schedule(dynamic)
    for (int i = 0; i < numx - 1; i++)
    {
        for (int j = 0; j < numy - 1; j++)
        {
            for (int k = 0; k < numz - 1; k++)
            {
                MarchingCube(i, j, k);
            }
        }
    }
    return triangles;
}

